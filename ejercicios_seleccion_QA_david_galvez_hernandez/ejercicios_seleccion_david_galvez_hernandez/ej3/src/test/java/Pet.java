
/*FUNCION PET PARA LA RECOGIDA DE LOS CAMPOS DE ID Y NAME*/
public class Pet {
    private int id;
    private String name;

    public Pet(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

}
